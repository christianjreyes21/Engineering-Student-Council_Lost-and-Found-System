package edu.ust.esc.model;

public class StudentEntryBean {
	private String lastName;
	private String firstName;
	private String middleName;
	private String yearLevelAndSection;
	private String studentNumber;
	private String emailAddress;
	private String category;
	private String concernsAndComplaints;
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getMiddleName() {
		return middleName;
	}
	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}
	public String getYearLevelAndSection() {
		return yearLevelAndSection;
	}
	public void setYearLevelAndSection(String yearLevelAndSection) {
		this.yearLevelAndSection = yearLevelAndSection;
	}
	public String getStudentNumber() {
		return studentNumber;
	}
	public void setStudentNumber(String studentNumber) {
		this.studentNumber = studentNumber;
	}
	public String getEmailAddress() {
		return emailAddress;
	}
	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getConcernnsAndComplaints() {
		return concernsAndComplaints;
	}
	public void setConcernsAndComplaints(String concernsAndComplaints) {
		this.concernsAndComplaints = concernsAndComplaints;
	}
	
		
}
